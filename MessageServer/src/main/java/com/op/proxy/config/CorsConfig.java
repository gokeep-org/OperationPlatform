@Configuration
public class CorsConfig extends WebMvcConfigurerAdapter {
    /**
     * 设置Cors跨域请求允许设置
     * @param registry
     */
    @Override
    public void addCorsMappings(CorsRegistry registry) {
        registry.addMapping("/**")
                .allowedOrigins("*")
                .allowedHeaders("*")
                .allowedMethods("*")
                .allowCredentials(true)
                .maxAge(36000);
    }
}
